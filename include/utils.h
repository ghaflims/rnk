void writel(unsigned int reg, unsigned int val);
unsigned int readl(unsigned int reg);

#define min(x, y) ({                            \
	typeof(x) _min1 = (x);                  \
	typeof(y) _min2 = (y);                  \
	(void) (&_min1 == &_min2);              \
	_min1 < _min2 ? _min1 : _min2; })

#define max(x, y) ({                            \
	typeof(x) _max1 = (x);                  \
	typeof(y) _max2 = (y);                  \
	(void) (&_max1 == &_max2);              \
	 _max1 > _max2 ? _max1 : _max2; })

#endif /* UTILS_H */
